              and so we thought everywhere else was the same. <br> <br>
              
              Family was true love, honesty and loyalty in human form <br>
              and so we thought everyone else was the same. <br> <br>

              Time passes and we grow. <br>
              We leave home to go to new places and meet new people. <br>
              We are happy because we are finally free
              of the ‘cage’ we call home. <br> <br>

              Little did we know that Mom and Dad <br>
              were only trying to protect us… <br>
              from the dark side of life. <br> <br>

              In our naivety, <br>
              we assume everyone is like us. <br>
              We think when they say,  “I love you”, <br>
              they mean it because Dad and Mom meant it. <br>
              We believe, we fall, we give of ourselves… <br>
              our best, our all, everything. <br> <br>

              But then, <br>
              nothing is hidden under the sun, <br>
              and everything under the sun would <br>eventually come to light. <br>
              The lies get exposed,  the truth breaks out, <br>
              We see their true colours… as they really are. <br>
              We see who they really are, <br>
              past their fake smiles,  their smooth <br>words,  their sleek moves <br>
              and their casual appearances. <br> <br>

              “Was it all a game,  some fun, a prank or a dream?” you ask yourself. <br>
              Because you can’t believe you were so true and they were the opposite. <br>
              You can’t believe they could fake it all along <br>
              and still act like nothing had happened.<br> <br>

              They smile, laugh and flatter you when they see you <br>
              and you do the same. <br>
              Only this time, you’ve learnt to fake it as well. <br>
              Because you’ve learnt that not everyone is like family <br>
              and not everywhere is like home <br>
